#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	ptrsize;

	ptrsize = count * size;
	ptr = malloc(ptrsize);
	if (ptr)
		ft_bzero(ptr, ptrsize);
	return (ptr);
}
