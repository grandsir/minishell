#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	size;
	int		i;

	size = ft_strlen(s1);
	str = (char *)malloc(size + 1);
	i = 0;
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
